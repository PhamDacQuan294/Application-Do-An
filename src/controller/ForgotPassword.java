    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText().trim();

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập email!");
            return;
        }

        boolean isExist = UserDAO.isExistUsername(email);

        if (!isExist) {
            JOptionPane.showMessageDialog(this, "Email không tồn tại trong hệ thống!");
        } else {
            controller.ResetPassword resetForm = new controller.ResetPassword(email);
            resetForm.setVisible(true);
            this.dispose(); 
           
        }
    }//GEN-LAST:event_jButton1ActionPerformed

